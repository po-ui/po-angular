/**
 * @usedBy PoDropdownComponent
 *
 * @description
 *
 * Enum utilizado para configurar o tamanho (`p-size`) do componente.
 */
export enum PoDropdownSize {
  /** Define o tamanho pequeno com altura de 32px, disponível para acessibilidade AA. */
  small = 'small',

  /** Define o tamanho padrão com altura de 44px. */
  medium = 'medium'
}
