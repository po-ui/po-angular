/**
 * @usedBy PoButtonGroupComponent
 *
 * @description
 *
 * Define o tipo de seleção dos botões do `po-button-group`.
 */
export enum PoButtonGroupToggle {
  /** Seleção múltipla. */
  Multiple = 'multiple',

  /** Seleção desabilitada. */
  None = 'none',

  /** Seleção única. */
  Single = 'single'
}
