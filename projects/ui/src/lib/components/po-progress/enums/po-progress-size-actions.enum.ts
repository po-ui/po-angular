/**
 * @usedBy PoProgressComponent
 *
 * @description
 *
 * Enum para configurar o tamanho dos elementos (`p-size-actions`) que compõe o componente, com excessão da barra de
 * progresso.
 */
export enum PoProgressSizeActions {
  /** Define o tamanho pequeno para os elementos, sendo ações (buttons) com altura de 32px e labels com altura de 16px,
   * disponível para acessibilidade AA. */
  small = 'small',
  /** Define o tamanho padrão para os elementos, sendo ações (buttons) com altura de 44px e labels com altura de 16px. */
  medium = 'medium'
}
