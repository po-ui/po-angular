/**
 * @usedBy PoDatepickerRangeComponent
 *
 * @description
 *
 * Interface para definição das literais usadas no `po-datepicker-range`.
 */
export interface PoDatepickerRangeLiterals {
  /** Data em formato inválido. */
  invalidFormat?: string;

  /** Data inicial maior que data final. */
  startDateGreaterThanEndDate?: string;

  /** Data inválida. */
  invalidDate?: string;

  /** Data fora do período. */
  dateOutOfPeriod?: string;

  /** Abrir calendário. */
  open?: string;

  /** Data inicial. */
  startDate?: string;

  /** Data final. */
  endDate?: string;

  /** Limpar campo. */
  clean?: string;
}
