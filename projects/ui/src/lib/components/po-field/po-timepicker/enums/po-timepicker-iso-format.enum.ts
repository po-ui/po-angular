/**
 * @usedBy PoTimepickerComponent
 *
 * @description
 *
 * *Enum* que define o padrão de formatação dos horários de saída do *model*.
 */
export enum PoTimepickerIsoFormat {
  /** Formato básico `HH:mm` (ex: `14:30`). */
  HourMinute = 'HH:mm',

  /** Formato com segundos `HH:mm:ss` (ex: `14:30:00`). */
  HourMinuteSecond = 'HH:mm:ss'
}
