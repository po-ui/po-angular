/**
 * @usedBy PoRadioComponent, PoRadioGroupComponent
 *
 * @description
 *
 * Enum para configurar o tamanho (`p-size`) do componente.
 */
export enum PoRadioSize {
  /** Define o tamanho pequeno de 16px, disponível para acessibilidade AA. */
  Small = 'small',

  /** Define o tamanho padrão de 24px. */
  Medium = 'medium',

  /** Define o tamanho grande de 32px. */
  Large = 'large'
}
